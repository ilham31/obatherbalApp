package com.example.ilham.obatherbal.compoundJava;

public class compoundModel {

    private String id,nama;

    public compoundModel(String id, String nama) {
        this.id = id;
        this.nama = nama;
    }

    public String getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }
}
